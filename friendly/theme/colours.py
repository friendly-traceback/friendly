from . import friendly_pygments
from ..my_gettext import current_lang
from .. import settings


def validate_color(color):
    _ = current_lang.translate
    if color is None or color == "None":
        return None
    if not isinstance(color, str):
        raise ValueError(_("`color` must be a string."))

    valid_characters = set("0123456789abcdef")
    color = color.lower().replace(" ", "")

    if color in NAMED_COLOURS:
        return NAMED_COLOURS[color]
    elif (
        color.startswith("#")
        and len(color) == 7
        and set(color[1:]).issubset(valid_characters)
    ):
        return color
    raise ValueError(_("Invalid color {color}").format(color=color))


def set_background_color(color):
    if color is None:
        color = friendly_pygments.set_pygments_background_color(None)
        settings.write(option="background", value=color)
        return color

    try:
        validate_color(color)
    except ValueError as e:
        print(e.args[0])
        return settings.read(option="background")
    else:
        color = friendly_pygments.set_pygments_background_color(color)
        settings.write(option="background", value=color)
        return color


def set_highlight(bg="#cc0000", fg="white"):
    if bg is None or fg is None:
        settings.write(option="highlight", value="use carets")
        return
    try:
        fg = validate_color(fg)
    except ValueError as e:
        print(e.args[0])
        return
    try:
        bg = validate_color(bg)
    except ValueError as e:
        print(e.args[0])
        return

    # use Rich format to save value
    settings.write(option="highlight", value=f"{fg} on {bg}")


def get_highlight():
    highlight = settings.read(option="highlight")
    if highlight == "use carets":
        return None
    elif highlight is None:
        bg, fg = friendly_pygments.get_pygments_error_token()
        return f"{fg} on {bg}"
    return highlight


